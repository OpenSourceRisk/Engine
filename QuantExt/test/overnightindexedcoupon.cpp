void runTest(QuantLib::Schedule schedule,
             QuantLib::ext::shared_ptr<OvernightIndex> index,
             Natural avgPeriod,
             std::map<Date, Real> ref,
             const std::string& testLabel, const Real tol) {

    Leg leg = QuantExt::OvernightLeg(schedule, index)
        .withNotionals(1)
        .withGearings(1.0)
        .withSpreads(0.0)
        .withInArrears(true)
        .withLastRecentPeriod(avgPeriod * Days)
        .withLastRecentPeriodCalendar(NullCalendar());

    Leg::const_iterator i;

    for (i = leg.begin(); i != leg.end(); ++i) {
        auto oi = QuantLib::ext::dynamic_pointer_cast<OvernightIndexedCoupon>(*i);
        Rate r = oi->rate() * 100;
        Rate refr = ref[oi->date()];
        BOOST_TEST_MESSAGE(testLabel << ": rate = " << r << ", reference rate = " << refr);
        BOOST_CHECK_SMALL(std::abs(r - refr), tol);
    }
}

